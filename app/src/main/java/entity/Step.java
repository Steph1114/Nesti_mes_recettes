package entity;

public class Step {

    int idParagraph;
    int orderParagraph;
    String contentParagraph;

    public int getIdParagraph() {
        return idParagraph;
    }

    public void setIdParagraph(int idParagraph) {
        this.idParagraph = idParagraph;
    }

    public int getOrderParagraph() {
        return orderParagraph;
    }

    public void setOrderParagraph(int orderParagraph) {
        this.orderParagraph = orderParagraph;
    }

    public String getContentParagraph() {
        return contentParagraph;
    }

    public void setContentParagraph(String contentParagraph) {
        this.contentParagraph = contentParagraph;
    }
}
